    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        // TODO add your handling code here:
        salvarAlterar="editar";
        DefaultTableModel modelo= (DefaultTableModel) this.jtProdutos.getModel();
        int linha=this.jtProdutos.getSelectedRow();
        try {  
            habilitarDesabilitar(true);
        int codigoProduto=Integer.parseInt(String.valueOf(this.jtProdutos.getValueAt(linha, 0))); 
         modeloProduto=controllerProduto.buscarProdutoController(codigoProduto);
         this.jtfCodigo.setText(String.valueOf(modeloProduto.getIdProduto()));
         this.jtfDescricao.setText(modeloProduto.getDescricao());
         this.jtfCodigoBarras.setText(modeloProduto.getCodigoDeBarras());
         this.jtfEstoque.setText(String.valueOf(modeloProduto.getEstoque()));
         this.jtfFornecedor.setText(modeloProduto.getFornecedor());
         this.jtfValor.setText(String.valueOf(modeloProduto.getValor()));
         this.jcbPaisOrigem.setSelectedItem(modeloProduto.getPaisDeOrigem());
         this.jcbSexo.setSelectedItem(modeloProduto.getSexo());
   
        } catch (Exception e) { 
            e.printStackTrace(); 
            this.setEnabled(false);
            new ViewSelecionarProduto(this).setVisible(true);
            habilitarDesabilitar(false);
        }
    }//GEN-LAST:event_jbEditarActionPerformed

