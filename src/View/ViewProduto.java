    private void salvarDadosProdutos() { 
        
        try {
            
        if(this.jtfDescricao.getText().isEmpty() || this.jcbSexo.getSelectedItem().equals("") ||this.jtfCodigoBarras.getText().isEmpty() ||
                this.jcbPaisOrigem.getSelectedItem() == null || this.jtfFornecedor.getText().isEmpty() || this.jtfEstoque.getText() == null  ||
             this.jtfValor.getText().equals(0)  ){ 
                 this.setEnabled(false);
                 new ViewPreencherTodosCamposProdutos(this).setVisible(true);
                
        }else{  
       // modeloProduto.setValor(100.25);
        modeloProduto.setValor(Double.parseDouble(this.jtfValor.getText()));    
        modeloProduto.setDescricao(this.jtfDescricao.getText());
        modeloProduto.setSexo(String.valueOf(this.jcbSexo.getSelectedItem()));
        modeloProduto.setCodigoDeBarras(this.jtfCodigoBarras.getText());
        modeloProduto.setPaisDeOrigem(String.valueOf(this.jcbPaisOrigem.getSelectedItem()));
        modeloProduto.setFornecedor(this.jtfFornecedor.getText());
        modeloProduto.setEstoque(Integer.parseInt(this.jtfEstoque.getText())); 
          
        
        if(controllerProduto.salvarProdutoController(modeloProduto)>0){ 
            this.setEnabled(false);
            new ViewSalvoProduto(this).setVisible(true);
           preencherTabelaProduto();
            limparDados();
        }else{ 
            this.setEnabled(false);
            new ViewErroSalvarProduto(this).setVisible(true);
        } 
        }
        
        
        } catch (Exception e) { 
            e.printStackTrace();
        }
   
    }
    
    private void editarDadosProdutos() {
        try {
            
        
        modeloProduto.setDescricao(this.jtfDescricao.getText());
        modeloProduto.setSexo(String.valueOf(this.jcbSexo.getSelectedItem()));
        modeloProduto.setCodigoDeBarras(this.jtfCodigoBarras.getText());
        modeloProduto.setPaisDeOrigem(String.valueOf(this.jcbPaisOrigem.getSelectedItem()));
        modeloProduto.setFornecedor(this.jtfFornecedor.getText());
        modeloProduto.setEstoque(Integer.parseInt(this.jtfEstoque.getText())); 
        modeloProduto.setValor(Double.parseDouble(this.jtfValor.getText()));
        if(this.jtfDescricao.getText().isEmpty() || this.jcbSexo.getSelectedItem().equals("") ||this.jtfCodigoBarras.getText().isEmpty() ||
            this.jcbPaisOrigem.getSelectedItem().equals(null) || this.jtfFornecedor.getText().isEmpty() || this.jtfEstoque.getText().equals(null)  ||
             this.jtfValor.getText().equals("null")  ){ 
                 this.setEnabled(false);
                 new ViewPreencherTodosCamposProdutos(this).setVisible(true);
                
        }else
        if(controllerProduto.editarProdutosController(modeloProduto)){ 
            this.setEnabled(false);
            new ViewEditarProduto(this).setVisible(true);
           preencherTabelaProduto();
            limparDados();
        }else{ 
            this.setEnabled(false);
            new ViewErroEditarProduto(this).setVisible(true);
        } 
        
        } catch (Exception e) { 
            e.printStackTrace();
        }
    }

